CREATE COLLATION IF NOT EXISTS public.nocase (provider = icu, deterministic = false, locale = 'und-u-ks-level2');

CREATE TABLE IF NOT EXISTS migration (
    id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name text NOT NULL UNIQUE,
    created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS year_status (name text PRIMARY KEY);
INSERT INTO year_status (name) VALUES ('open'), ('closed'), ('ongoing')
    ON CONFLICT DO NOTHING;

CREATE TABLE IF NOT EXISTS show_status (name text PRIMARY KEY);
INSERT INTO show_status (name) VALUES ('none'), ('draw'), ('partial'), ('full')
    ON CONFLICT DO NOTHING;

CREATE TABLE IF NOT EXISTS account_role (
    name text PRIMARY KEY,
    can_edit boolean NOT NULL DEFAULT false,
    can_view_restricted boolean NOT NULL DEFAULT false
);
INSERT INTO account_role (name, can_edit, can_view_restricted) VALUES
    ('user', false, false),
    ('editor', true, false),
    ('admin', true, true),
    ('owner', true, true)
    ON CONFLICT DO NOTHING;

CREATE TABLE IF NOT EXISTS account (
    id bigint PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    username text UNIQUE COLLATE public.nocase,
    email text,
    password bytea,
    salt bytea,
    approved boolean DEFAULT false,
    role text DEFAULT 'user' REFERENCES account_role (name) ON UPDATE RESTRICT ON DELETE RESTRICT
);

CREATE TABLE IF NOT EXISTS session (
    id bigint PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    user_id bigint NOT NULL REFERENCES account (id) ON UPDATE RESTRICT ON DELETE RESTRICT,
    session_id uuid NOT NULL,
    created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
    expires_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS country (
    id text PRIMARY KEY COLLATE public.nocase,
    name text UNIQUE COLLATE public.nocase,
    is_participating boolean,
    pot smallint,
    available_from smallint DEFAULT 0,
    available_until smallint DEFAULT 9999,
    priority integer,
    cc3 text NOT NULL COLLATE public.nocase
);

CREATE TABLE IF NOT EXISTS year (
    id bigint PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    status text REFERENCES year_status (name) ON UPDATE RESTRICT ON DELETE RESTRICT,
    host_id text COLLATE public.nocase REFERENCES country (id) ON UPDATE RESTRICT ON DELETE RESTRICT,
    special_name text,
    special_short_name text COLLATE public.nocase,
    CONSTRAINT year_special_name_check
    CHECK ((id >= 0 AND special_name IS NULL AND special_short_name IS NULL)
    OR (id < 0 AND special_name IS NOT NULL AND special_short_name IS NOT NULL)),
    CONSTRAINT year_special_short_name_key UNIQUE (special_short_name)
);

CREATE TABLE IF NOT EXISTS alternative_name (
    id bigint PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    country_id text COLLATE public.nocase REFERENCES country (id) ON UPDATE RESTRICT ON DELETE RESTRICT,
    from_year_id bigint,
    to_year_id bigint,
    name text
);

CREATE TABLE IF NOT EXISTS language (
