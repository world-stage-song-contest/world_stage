CREATE OR REPLACE FUNCTION redact_ip_address(p_show_id bigint)
RETURNS void AS $$
BEGIN
    UPDATE vote_set
    SET ip_address = NULL
    WHERE show_id = p_show_id;
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE FUNCTION trigger_redact_ip_address()
RETURNS TRIGGER AS $$
BEGIN
    PERFORM redact_ip_address(NEW.id);
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE FUNCTION refresh_show_results(p_show_id bigint)
RETURNS void AS $$
DECLARE
    v_max_point integer;
    v_inserted integer;
BEGIN
    SELECT MAX(p.score)
    INTO v_max_point
    FROM show sh
    JOIN point p ON p.point_system_id = sh.point_system_id
    WHERE sh.id = p_show_id;

    DELETE FROM country_show_results WHERE show_id = p_show_id;

    WITH si AS (
    SELECT sh.id, sh.year_id, sh.dtf, sh.sc, sh.special,
    sh.short_name, sh.show_name
    FROM show sh
    WHERE sh.id = p_show_id
    ),
    all_vote_sets AS (
    SELECT vs.*
    FROM vote_set vs
    WHERE vs.show_id = p_show_id
    ),
    voters_by_country AS (
    SELECT vs.country_id, COUNT(*) AS cnt
    FROM all_vote_sets vs
    WHERE vs.country_id IS NOT NULL
    GROUP BY vs.country_id
    ),
    totals AS (
    SELECT COUNT(*) AS total_valid FROM all_vote_sets
    ),
    point_counts AS (
    SELECT s.id AS song_id, s.country_id, v.score, COUNT(*) AS cnt
    FROM si
    JOIN song_show ss ON ss.show_id = si.id
    JOIN song s ON s.id = ss.song_id
    JOIN vote v ON v.song_id = s.id
    JOIN all_vote_sets vs ON vs.id = v.vote_set_id
    WHERE
    -- Modern rules (1979+) and specials (negative year_id): all votes count
    (si.year_id IS NULL OR si.year_id < 0 OR si.year_id >= 1979)
    -- 1965–1978: a country cannot vote for its own entry
    OR (si.year_id BETWEEN 1965 AND 1978
    AND vs.country_id IS DISTINCT FROM s.country_id)
    -- Pre-1965 (except 1960): also no self-votes
    OR (si.year_id >= 0 AND si.year_id < 1965 AND si.year_id <> 1960
    AND vs.country_id IS DISTINCT FROM s.country_id)
    -- 1960 special rule: self-vote allowed only if score = 1
    OR (si.year_id = 1960 AND (
    vs.country_id IS DISTINCT FROM s.country_id
    OR (vs.country_id = s.country_id AND v.score = 1)
    ))
    GROUP BY s.id, s.country_id, v.score
    ),
    aggregated AS (
    SELECT
    s.country_id,
    c.name AS country_name,
    si.id AS show_id,
    s.id AS song_id,
    ss.running_order,
    si.show_name,
    si.short_name,
    si.year_id,
    si.dtf, si.sc, si.special,
    COALESCE(SUM(pc.score * pc.cnt), 0) AS total_points,
    COALESCE(SUM(pc.cnt), 0) AS total_votes_received,
    COALESCE(
    JSONB_OBJECT_AGG(pc.score::text, pc.cnt ORDER BY pc.score DESC)
    FILTER (WHERE pc.score IS NOT NULL),
    '{}'::jsonb
    ) AS point_distribution,
    COALESCE(
    STRING_AGG(
    LPAD(pc.score::text, 3, '0') || ':' || LPAD(pc.cnt::text, 3, '0'),
    ',' ORDER BY pc.score DESC
    ) FILTER (WHERE pc.score IS NOT NULL),
    ''
    ) AS countback_string,
    (SELECT total_valid FROM totals) AS total_voters,
    CASE
    WHEN v_max_point IS NULL THEN 0
    WHEN si.year_id IS NULL OR si.year_id < 0 OR si.year_id >= 1979
    THEN v_max_point * (SELECT total_valid FROM totals)
    WHEN (si.year_id BETWEEN 1966 AND 1978)
    OR (si.year_id = 1965 AND si.short_name = 'f')
    THEN v_max_point * GREATEST(
    (SELECT total_valid FROM totals) - COALESCE(vbc.cnt, 0), 0)
    WHEN si.year_id = 1960
    THEN v_max_point * GREATEST(
    (SELECT total_valid FROM totals) - COALESCE(vbc.cnt, 0), 0)
    + COALESCE(vbc.cnt, 0)
    ELSE
    v_max_point * GREATEST(
    (SELECT total_valid FROM totals) - COALESCE(vbc.cnt, 0), 0)
    END AS max_possible_points_calc
    FROM si
    JOIN song_show ss ON ss.show_id = si.id
    JOIN song s ON s.id = ss.song_id
    JOIN country c ON c.id = s.country_id
    LEFT JOIN point_counts pc ON pc.song_id = s.id
    LEFT JOIN voters_by_country vbc ON vbc.country_id = s.country_id
    WHERE c.is_participating
    GROUP BY
    s.country_id, c.name, si.id, s.id, ss.running_order,
    si.show_name, si.short_name, si.year_id,
    si.dtf, si.sc, si.special, vbc.cnt
    ),
    ranked AS (
    SELECT
    a.*,
    DENSE_RANK() OVER (
    ORDER BY total_points DESC, total_votes_received DESC, countback_string DESC
    ) AS place,
    COUNT(*) OVER () AS total_countries
    FROM aggregated a
    )
    INSERT INTO country_show_results (
    country_id, country_name, show_id, show_name, short_name, year_id,
    song_id, running_order,
    total_points, total_votes_received, point_distribution,
    place, total_countries, placement_percentage,
    max_possible_points, points_percentage,
    entry_status, max_pts, total_voters
    )
    SELECT
    r.country_id,
    r.country_name,
    r.show_id,
    r.show_name,
    r.short_name,
    r.year_id,
    r.song_id,
    r.running_order,
    r.total_points,
    r.total_votes_received,
    r.point_distribution,
    r.place,
    r.total_countries,
    ROUND(
    CASE
    WHEN r.place = r.total_countries THEN 0
    WHEN r.total_countries > 1
    THEN ((r.total_countries - r.place)::numeric
    / (r.total_countries - 1)) * 100
    ELSE 100
    END, 2
    ) AS placement_percentage,
    COALESCE(r.max_possible_points_calc, 0) AS max_possible_points,
    ROUND(
    COALESCE(
    r.total_points::numeric / NULLIF(r.max_possible_points_calc::numeric, 0),
    0
    ) * 100, 2
    ) AS points_percentage,
    CASE
    WHEN r.short_name = 'f' THEN NULL
    WHEN r.place <= COALESCE(r.dtf, 0) THEN 'dtf'
    WHEN r.place <= COALESCE(r.dtf, 0) + COALESCE(r.special, 0) THEN 'special'
    WHEN r.place <= COALESCE(r.dtf, 0) + COALESCE(r.special, 0)
    + COALESCE(r.sc, 0) THEN 'sc'
    ELSE 'nq'
    END AS entry_status,
    v_max_point AS max_pts,
    r.total_voters
    FROM ranked r;

    GET DIAGNOSTICS v_inserted = ROW_COUNT;
    RAISE NOTICE 'Refreshed results for show %; rows=%', p_show_id, v_inserted;
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE FUNCTION trigger_refresh_show_results_from_vote()
RETURNS TRIGGER AS $$
DECLARE
    v_show_id integer;
BEGIN
    SELECT show_id INTO v_show_id
    FROM vote_set
    WHERE id = COALESCE(NEW.vote_set_id, OLD.vote_set_id);

    IF v_show_id IS NOT NULL THEN
    PERFORM refresh_show_results(v_show_id);
    END IF;
    RETURN COALESCE(NEW, OLD);
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE FUNCTION trigger_refresh_show_results_from_vote_set()
RETURNS TRIGGER AS $$
DECLARE
    v_show_id integer;
BEGIN
    v_show_id := COALESCE(NEW.show_id, OLD.show_id);
    IF v_show_id IS NOT NULL THEN
    PERFORM refresh_show_results(v_show_id);
    END IF;
    RETURN COALESCE(NEW, OLD);
END;
$$ LANGUAGE plpgsql;

-- Recompute country_year_results for a year using a three-tier ranking.
CREATE OR REPLACE FUNCTION refresh_year_results(p_year_id bigint)
RETURNS void
LANGUAGE plpgsql
AS $$
BEGIN
    DELETE FROM country_year_results WHERE year_id = p_year_id;

    WITH finals AS (
    SELECT song_id, year_id, place AS f_place
    FROM country_show_results
    WHERE short_name = 'f' AND year_id = p_year_id
    ),
    sc_entries AS (
    SELECT song_id, year_id, place AS sc_place
    FROM country_show_results
    WHERE short_name = 'sc' AND year_id = p_year_id
    ),
    sf_entries AS (
    SELECT
    csr.song_id,
    csr.year_id,
    csr.points_percentage AS sf_pct,
    csr.total_voters AS sf_voters,
    csr.running_order AS sf_running_order,
    COALESCE(
    NULLIF(regexp_replace(csr.short_name, '\D', '', 'g'), '')::int,
    0
    ) AS sf_index,
    (
    SELECT STRING_AGG(
    LPAD(j.key, 3, '0') || ':' || LPAD(j.value, 3, '0'),
    ',' ORDER BY j.key::integer DESC
    )
    FROM jsonb_each_text(csr.point_distribution) AS j
    ) AS sf_countback
    FROM country_show_results csr
    WHERE (csr.short_name LIKE 'sf%' OR csr.short_name = 'sf')
    AND csr.year_id = p_year_id
    ),
    base AS (
    SELECT DISTINCT country_id, country_name, song_id, year_id
    FROM country_show_results
    WHERE year_id = p_year_id
    ),
    rank_space AS (
    SELECT
    b.country_id,
    b.country_name,
    b.year_id,
    b.song_id,
    CASE
    WHEN f.song_id IS NOT NULL THEN 1
    WHEN sc.song_id IS NOT NULL THEN 2
    WHEN sf.song_id IS NOT NULL THEN 3
    ELSE 4
    END AS tier,
    f.f_place,
    sc.sc_place,
    sf.sf_pct,
    sf.sf_voters,
    sf.sf_countback,
    sf.sf_running_order,
    sf.sf_index
    FROM base b
    LEFT JOIN finals f ON f.song_id = b.song_id AND f.year_id = b.year_id
    LEFT JOIN sc_entries sc ON sc.song_id = b.song_id AND sc.year_id = b.year_id
    LEFT JOIN sf_entries sf ON sf.song_id = b.song_id AND sf.year_id = b.year_id
    ),
    ranked_finals AS (
    SELECT *,
    ROW_NUMBER() OVER (ORDER BY f_place ASC) AS rank_in_tier
    FROM rank_space
    WHERE tier = 1
    ),
    ranked_sc AS (
    SELECT *,
    ROW_NUMBER() OVER (ORDER BY sc_place ASC) AS rank_in_tier
    FROM rank_space
    WHERE tier = 2
    ),
    ranked_sf AS (
    SELECT *,
    ROW_NUMBER() OVER (
    ORDER BY
    sf_pct DESC,
    sf_voters DESC,
    sf_countback DESC NULLS LAST,
    sf_running_order ASC NULLS LAST,
    sf_index ASC
    ) AS rank_in_tier
    FROM rank_space
    WHERE tier = 3
    ),
    ranked_other AS (
    SELECT *,
    ROW_NUMBER() OVER (ORDER BY song_id) AS rank_in_tier
    FROM rank_space
    WHERE tier = 4
    ),
    tier_sizes AS (
    SELECT
    (SELECT COUNT(*) FROM ranked_finals) AS n_f,
    (SELECT COUNT(*) FROM ranked_sc) AS n_sc,
    (SELECT COUNT(*) FROM ranked_sf) AS n_sf
    ),
    all_ranked AS (
    SELECT country_id, country_name, year_id, song_id,
    rank_in_tier::integer AS place
    FROM ranked_finals

    UNION ALL

    SELECT country_id, country_name, year_id, song_id,
    ((SELECT n_f FROM tier_sizes) + rank_in_tier)::integer
    FROM ranked_sc

    UNION ALL

    SELECT country_id, country_name, year_id, song_id,
    ((SELECT n_f + n_sc FROM tier_sizes) + rank_in_tier)::integer
    FROM ranked_sf

    UNION ALL

    SELECT country_id, country_name, year_id, song_id,
    ((SELECT n_f + n_sc + n_sf FROM tier_sizes) + rank_in_tier)::integer
    FROM ranked_other
    ),
    totals AS (
    SELECT COUNT(*) AS total_countries FROM all_ranked
    )
    INSERT INTO country_year_results
    (country_id, country_name, year_id, song_id,
    place, total_countries, placement_percentage)
    SELECT
    ar.country_id,
    ar.country_name,
    ar.year_id,
    ar.song_id,
    ar.place,
    t.total_countries,
    CASE
    WHEN t.total_countries = 1 THEN 100
    ELSE ROUND(
    ((t.total_countries - ar.place)::numeric
    / (t.total_countries - 1)::numeric) * 100,
    3)
    END AS placement_percentage
    FROM all_ranked ar
    CROSS JOIN totals t
    ORDER BY ar.place;
END;
$$;

CREATE OR REPLACE FUNCTION compute_country_year_results()
RETURNS trigger AS $func$
BEGIN
    IF NEW.status = 'closed' AND OLD.status IS DISTINCT FROM 'closed' THEN
    PERFORM refresh_year_results(NEW.id);
    END IF;
    RETURN NEW;
END;
