CREATE TABLE IF NOT EXISTS song_language (
    id bigint PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    song_id bigint REFERENCES song (id) ON UPDATE RESTRICT ON DELETE RESTRICT,
    language_id bigint REFERENCES language (id) ON UPDATE RESTRICT ON DELETE RESTRICT,
