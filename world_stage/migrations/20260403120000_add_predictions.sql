BEGIN;

CREATE TABLE prediction_set (
    id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    user_id integer NOT NULL REFERENCES account (id) ON UPDATE RESTRICT ON DELETE RESTRICT,
    show_id integer NOT NULL REFERENCES show (id) ON UPDATE RESTRICT ON DELETE RESTRICT,
    created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UNIQUE(user_id, show_id)
);

CREATE TABLE prediction (
    id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    set_id integer NOT NULL REFERENCES prediction_set (id) ON UPDATE RESTRICT ON DELETE CASCADE,
    song_id integer NOT NULL REFERENCES song (id) ON UPDATE RESTRICT ON DELETE RESTRICT,
    position integer NOT NULL,
    UNIQUE(set_id, song_id),
    UNIQUE(set_id, position)
);

COMMIT;
